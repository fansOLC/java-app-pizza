package org.project.pojo;

public class Zpsd {
	private Integer id;
	private String zpsdCode;
	private String zpsdName;
	private String prefix;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getZpsdCode() {
		return zpsdCode;
	}
	public void setZpsdCode(String zpsdCode) {
		this.zpsdCode = zpsdCode;
	}
	public String getZpsdName() {
		return zpsdName;
	}
	public void setZpsdName(String zpsdName) {
		this.zpsdName = zpsdName;
	}
	public String getPrefix() {
		return prefix;
	}
	public void setPrefix(String prefix) {
		this.prefix = prefix;
	}
	
}
